     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $categories = Category::all();
        $contact = Config::orderByDesc('config_id')->first();
        $message = new Message();
        $message->mess_name = $request->mess_name;
        $message->mess_email = $request->mess_email;
        $message->mess_phone = $request->mess_phone;
        $message->mess_message = $request->mess_message;
        $message->save();
        return view('client.contact.SendMessSuccess', compact('contact', 'categories'));

    }

